#!/bin/bash
# –°–∫—Ä–∏–ø—Ç –¥–ª—è –ø—Ä–æ–≤–µ—Ä–∫–∏ –∏ –∏—Å–ø—Ä–∞–≤–ª–µ–Ω–∏—è —Ñ–æ—Ä–º–∞—Ç–∞ —Ñ–∞–π–ª–∞ .env

ENV_FILE=${1:-".env.prod"}

if [ ! -f "$ENV_FILE" ]; then
    echo "‚ùå –û—à–∏–±–∫–∞: —Ñ–∞–π–ª $ENV_FILE –Ω–µ –Ω–∞–π–¥–µ–Ω!"
    exit 1
fi

echo "üîç –ü—Ä–æ–≤–µ—Ä–∫–∞ —Ñ–æ—Ä–º–∞—Ç–∞ —Ñ–∞–π–ª–∞ $ENV_FILE..."

# –ü—Ä–æ–≤–µ—Ä–∫–∞ –Ω–∞ –Ω–∞–ª–∏—á–∏–µ –Ω–µ—Å–æ–≥–ª–∞—Å–æ–≤–∞–Ω–Ω—ã—Ö –∫–∞–≤—ã—á–µ–∫
SINGLE_QUOTES=$(grep -c "'" "$ENV_FILE")
DOUBLE_QUOTES=$(grep -c '"' "$ENV_FILE")

echo "–ù–∞–π–¥–µ–Ω–æ –æ–¥–∏–Ω–∞—Ä–Ω—ã—Ö –∫–∞–≤—ã—á–µ–∫: $SINGLE_QUOTES"
echo "–ù–∞–π–¥–µ–Ω–æ –¥–≤–æ–π–Ω—ã—Ö –∫–∞–≤—ã—á–µ–∫: $DOUBLE_QUOTES"

# –°–æ–∑–¥–∞–µ–º –≤—Ä–µ–º–µ–Ω–Ω—É—é –∫–æ–ø–∏—é –¥–ª—è –±–µ–∑–æ–ø–∞—Å–Ω–æ—Å—Ç–∏
cp "$ENV_FILE" "${ENV_FILE}.bak"

# –ü—Ä–æ–≤–µ—Ä–∫–∞ –Ω–∞ –Ω–∞–ª–∏—á–∏–µ –Ω–µ—Å–æ–≥–ª–∞—Å–æ–≤–∞–Ω–Ω—ã—Ö –º–Ω–æ–≥–æ—Å—Ç—Ä–æ—á–Ω—ã—Ö –∑–Ω–∞—á–µ–Ω–∏–π
if grep -q '`' "$ENV_FILE"; then
    echo "‚ö†Ô∏è –û–±–Ω–∞—Ä—É–∂–µ–Ω—ã –æ–±—Ä–∞—Ç–Ω—ã–µ –∫–∞–≤—ã—á–∫–∏ (`) –∫–æ—Ç–æ—Ä—ã–µ –º–æ–≥—É—Ç –≤—ã–∑—ã–≤–∞—Ç—å –æ—à–∏–±–∫–∏ –ø—Ä–∏ –æ–±—Ä–∞–±–æ—Ç–∫–µ —Ñ–∞–π–ª–∞"
    sed -i 's/`/"/g' "$ENV_FILE"
    echo "‚úÖ –û–±—Ä–∞—Ç–Ω—ã–µ –∫–∞–≤—ã—á–∫–∏ –∑–∞–º–µ–Ω–µ–Ω—ã –Ω–∞ –¥–≤–æ–π–Ω—ã–µ"
fi

# –ü—Ä–æ–≤–µ—Ä–∫–∞ –Ω–∞ –Ω–∞–ª–∏—á–∏–µ –ø—Ä–æ–±–µ–ª–æ–≤ –≤–æ–∫—Ä—É–≥ –∑–Ω–∞–∫–∞ —Ä–∞–≤–µ–Ω—Å—Ç–≤–∞
if grep -q ' *= *' "$ENV_FILE"; then
    echo "‚ö†Ô∏è –û–±–Ω–∞—Ä—É–∂–µ–Ω—ã –ø—Ä–æ–±–µ–ª—ã –≤–æ–∫—Ä—É–≥ –∑–Ω–∞–∫–∞ —Ä–∞–≤–µ–Ω—Å—Ç–≤–∞, –∫–æ—Ç–æ—Ä—ã–µ –º–æ–≥—É—Ç –≤—ã–∑—ã–≤–∞—Ç—å –æ—à–∏–±–∫–∏"
    sed -i 's/ *= */=/g' "$ENV_FILE"
    echo "‚úÖ –ü—Ä–æ–±–µ–ª—ã –≤–æ–∫—Ä—É–≥ –∑–Ω–∞–∫–∞ —Ä–∞–≤–µ–Ω—Å—Ç–≤–∞ —É–¥–∞–ª–µ–Ω—ã"
fi

# –ü—Ä–æ–≤–µ—Ä–∫–∞ –Ω–∞ –ø—É—Å—Ç—ã–µ —Å—Ç—Ä–æ–∫–∏ –∏ —Å—Ç—Ä–æ–∫–∏ —Ç–æ–ª—å–∫–æ —Å –∫–æ–º–º–µ–Ω—Ç–∞—Ä–∏—è–º–∏
echo "üîç –°–æ–∑–¥–∞–Ω–∏–µ —á–∏—Å—Ç–æ–π –≤–µ—Ä—Å–∏–∏ —Ñ–∞–π–ª–∞ –±–µ–∑ –∫–æ–º–º–µ–Ω—Ç–∞—Ä–∏–µ–≤ –∏ –ø—É—Å—Ç—ã—Ö —Å—Ç—Ä–æ–∫..."
grep -v '^#' "$ENV_FILE" | grep -v '^$' > "${ENV_FILE}.clean"

echo "‚úÖ –ü—Ä–æ–≤–µ—Ä–∫–∞ –∏ –æ—á–∏—Å—Ç–∫–∞ –∑–∞–≤–µ—Ä—à–µ–Ω—ã."
echo "- –ò—Å—Ö–æ–¥–Ω—ã–π —Ñ–∞–π–ª —Å–æ—Ö—Ä–∞–Ω–µ–Ω –∫–∞–∫: ${ENV_FILE}.bak"
echo "- –û—á–∏—â–µ–Ω–Ω–∞—è –≤–µ—Ä—Å–∏—è —Å–æ—Ö—Ä–∞–Ω–µ–Ω–∞ –∫–∞–∫: ${ENV_FILE}.clean"
echo

echo "üìã –ü—Ä–æ–≤–µ—Ä–∫–∞ —Å–∏–Ω—Ç–∞–∫—Å–∏—Å–∞ —ç–∫—Å–ø–æ—Ä—Ç–∞ –ø–µ—Ä–µ–º–µ–Ω–Ω—ã—Ö:"
while IFS= read -r line || [[ -n "$line" ]]; do
    if [[ -z "$line" || "$line" =~ ^# ]]; then
        continue
    fi
    if ! [[ "$line" =~ ^[A-Za-z0-9_]+=.* ]]; then
        echo "‚ùå –ù–µ–ø—Ä–∞–≤–∏–ª—å–Ω—ã–π —Ñ–æ—Ä–º–∞—Ç —Å—Ç—Ä–æ–∫–∏: $line"
    fi
done < "${ENV_FILE}.clean"

echo "‚úÖ –ü—Ä–æ–≤–µ—Ä–∫–∞ –∑–∞–≤–µ—Ä—à–µ–Ω–∞!"
